var gallonsToLiters = function (num) {
  return num * 3.79;
};

var litersToGallons = function (num) {
  return num / 3.79;
};

var tablespoonToTeaspoon = function (num) {
  return num * 1/3;
};

var teaspoonToTablespoon = function (num) {
  return num * 3;
};

var cupsToOunces = function (num) {
  return num * 8;
};

var ouncesToCups = function (num) {
  return num / 8;
};
